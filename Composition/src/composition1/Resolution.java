package composition1;

public class Resolution {
	
	private int n;
	private int boy;
	
	public Resolution(int n, int boy) {
		super();
		this.n = n;
		this.boy = boy;
	}

	public int getN() {
		return n;
	}

	public void setN(int n) {
		this.n = n;
	}

	public int getBoy() {
		return boy;
	}

	public void setBoy(int boy) {
		this.boy = boy;
	}
	
}
